#include <stdio.h>

int main() {
    int a = 10;
    int b = 5;
    int result;

    // Add
    result = a + b;
    printf("Add: %d\n", result);

    // Sub
    result = a - b;
    printf("Sub: %d\n", result);

    // Multip
    result = a * b;
    printf("Multi: %d\n", result);

    // Div
    result = a / b;
    printf("Div: %d\n", result);

    // Mod
    result = a % b;
    printf("Mod: %d\n", result);

    return 0;
}
