#include <stdio.h>

int main() {
    int integerVariable = 10;
    char charVariable = 'A';
    float floatVariable = 3.14;
    double doubleVariable = 3.14159265359;
    short shortVariable = 100;
    long longVariable = 1000000;

    

    printf("Integer: %d\n", integerVariable);
    printf("Char: %c\n", charVariable);
    printf("Float: %f\n", floatVariable);
    printf("Double: %lf\n", doubleVariable);
    printf("Short: %d\n", shortVariable);
    printf("Long: %ld\n", longVariable);
   

    return 0;
}
